using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MethodExercise
{
    class Math
    {
        public int Addition(int numInput)
        {
            int numOutput = (numInput + 1);
            return numOutput;
        }

        public int Subtraction(int numInput)
        {
            int numOutput = (numInput - 1);
            return numOutput;
        }
        public int Multiply(int numInput)
        {
            int numOutput = (numInput * 1);
            return numOutput;
        }
    }

}
